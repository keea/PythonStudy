# 2개의 정수를 받아서 사칙연산 후 결과출력

inputNum1 = int(input("첫 번째 정수 입력 : "))
inputNum2 = int(input("두 번째 정수 입력 : "))

print(inputNum1, "+", inputNum2, "=", inputNum1+inputNum2)
print(inputNum1, "-", inputNum2, "=", inputNum1-inputNum2)
print(inputNum1, "*", inputNum2, "=", inputNum1*inputNum2)
print(inputNum1, "/", inputNum2, "=", inputNum1/inputNum2)
